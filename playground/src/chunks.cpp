bool Chunk::in_bound(const glm::vec3& pos)
{
  return pos.x >= 0 && pos.x < kSize
    && pos.y >= 0 && pos.y < kSize
    && pos.z >= 0 && pos.z < kSize;
}

void Chunk::generate_model()
{
  constexpr float kSurface = 0.5f, kMinValue = 0.0f, kMaxValue = 1.0f;

  std::vector<axgl::opengl::Vertex3> vertices;
  for (int x = -1; x < kSize; ++x)
  {
    for (int y = -1; y < kSize; ++y)
    {
      for (int z = -1; z < kSize; ++z)
      {
        glm::ivec3 cube[8] = {
            glm::ivec3(x, y, z),
            glm::ivec3(x + 1, y, z),
            glm::ivec3(x + 1, y, z + 1),
            glm::ivec3(x, y, z + 1),
            glm::ivec3(x, y + 1, z),
            glm::ivec3(x + 1, y + 1, z),
            glm::ivec3(x + 1, y + 1, z + 1),
            glm::ivec3(x, y + 1, z + 1)
        };

        int cubeIndex = 0;
        for (int i = 0; i < 8; ++i)
          if (in_bound(cube[i]) && data_[cube[i].x][cube[i].y][cube[i].z] > kSurface)
            cubeIndex |= (1 << i);

        if (cubeIndex != 0)
        {
          int vertCount = 0;
          for (int i = 0; i < 16; ++i)
          {
            if (kMarchingCubeTriangles[cubeIndex][i] == -1)
              break;

            glm::ivec3 p1 = cube[kCIAFE[kMarchingCubeTriangles[cubeIndex][i]]];
            glm::ivec3 p2 = cube[kCIBFE[kMarchingCubeTriangles[cubeIndex][i]]];
            float v1 = in_bound(p1) ? data_[p1.x][p1.y][p1.z] : kMinValue;
            float v2 = in_bound(p2) ? data_[p2.x][p2.y][p2.z] : kMinValue;

            glm::vec3 pos = glm::vec3(p1) + (kSurface - v1) * (glm::vec3(p2) - glm::vec3(p1)) / (v2 - v1);
            // glm::vec3 pos = (p1 + p2) / 2.0f;
            // glm::vec3 color(8.0f, 8.0f, 8.0f);
            vertices.push_back({ pos });

            if (++vertCount == 3)
            {
              vertCount = 0;
              size_t vertSize = vertices.size();
              glm::vec3 vert1 = vertices[vertSize - 3].pos;
              glm::vec3 vert2 = vertices[vertSize - 2].pos;
              glm::vec3 vert3 = vertices[vertSize - 1].pos;

              glm::vec3 U = vert2 - vert1;
              glm::vec3 V = vert3 - vert1;

              glm::vec3 normal(
                U.y * U.z - U.z * V.y,
                U.z * V.x - U.x * V.z,
                U.x * V.y - U.y * V.x
              );
              vertices[vertSize - 3].normal = normal;
              vertices[vertSize - 2].normal = normal;
              vertices[vertSize - 1].normal = normal;

              float r = static_cast<float>(rand());
              float LO = 0.5f;
              float HI = 0.8f;
              glm::vec3 color(LO + r / (static_cast <float> (RAND_MAX / (HI - LO))));
              vertices[vertSize - 3].color = color;
              vertices[vertSize - 2].color = color;
              vertices[vertSize - 1].color = color;
            }
          }
        }
      }
    }
  }

  model_ = std::make_shared<axgl::opengl::Model>();
  model_->add_mesh(std::make_shared<axgl::opengl::Mesh>(vertices));
  model_->translation.y -= 3;
  model_->translation.x += kSize / 2.0f;
  model_->translation.z -= kSize / 2.0f;
  model_->rotation.z = static_cast<float>(M_PI);
}

void Chunk::render(const axgl::world::RenderContext& context)
{
  using namespace axgl::opengl::shader;

  PhongShader::LightColor light_color{ glm::vec3(0.3f), glm::vec3(1.0f), glm::vec3(1.0f) };

  std::vector<std::shared_ptr<PhongShader::Light>> lights = {
    std::make_shared<PhongShader::Light>(glm::vec3(0.2f, -1.0f, 1.2f), light_color)
  };

  PhongShader::Uniforms uniforms;
  uniforms.lights = lights;
  uniforms.camera_pos = context.camera->position;
  uniforms.model = model_->model_matrix();
  uniforms.mvp = context.pv * uniforms.model;
  uniforms.shininess = 16.0f;
  uniforms.specular = 0.2f;

  shader_->use_program();
  shader_->use_uniforms(uniforms);

  model_->render(shader_);
}

float Chunk::noise(const glm::vec3& block_pos)
{
  static PerlinNoise noise;
  static constexpr int kOctaves = 3;
  static constexpr int kLacunarity = 2;
  static constexpr float kPersistance = 0.5f;
  static constexpr float kScale = 5.0f;

  glm::vec3 pos = block_pos / kScale;
  glm::vec3 chunk_pos = pos / (float)kSize;
  double steepness = pow(noise.value(chunk_pos.x, chunk_pos.y, chunk_pos.z) + 0.5, 9) + 0.5;
  double value = -block_pos.y * steepness;
  for (int i = 0; i < kOctaves; ++i)
  {
    pos *= pow(kLacunarity, i) * pow(kPersistance, i);
    value += noise.value(pos.x, pos.y, pos.z) * kSize * 0.5;
  }
  // return value > kScale * 0.5;
  return static_cast<float>(value);
}

void Chunk::generate_data(float(*data)[kSize][kSize])
{
  for (int x = 0; x < kSize; ++x)
    for (int y = 0; y < kSize; ++y)
      for (int z = 0; z < kSize; ++z)
        data[x][y][z] = noise({ x, y, z });
}

